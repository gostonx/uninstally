def translate(key, lang, en_value):
    """Return translation for key in language, or fall back to en_value."""
    if lang in T and key in T[lang]:
        return T[lang][key]
    # For format strings (containing %), keep the en_value with position markers
    if "%" in key or "%" in en_value:
        return en_value
    # For string catalog auto-keys (starting with _), return en_value
    if key.startswith("_"):
        return en_value
    # For unknown keys, fall back gracefully to English
    return en_value

def main():
    path = Path(__file__).parent.parent / "Uninstally" / "Localizable.xcstrings"
    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)

    strings = data.get("strings", {})
    fixed_tags = 0
    added_langs = 0
    total_entries = 0

    for key, entry in list(strings.items()):
        if not isinstance(entry, dict) or not key:
            continue
        locs = entry.setdefault("localizations", {})

        # Ensure English exists
        en_unit = locs.get("en", {}).get("stringUnit", {})
        en_value = en_unit.get("value", key)
        if "en" not in locs or not isinstance(locs.get("en"), dict):
            locs["en"] = {"stringUnit": {"state": "translated", "value": key}}
            en_value = key
        elif not en_value:
            locs["en"]["stringUnit"] = {"state": "translated", "value": key}
            en_value = key

        for lang_code in LANGS:
            if lang_code not in locs or not isinstance(locs.get(lang_code), dict):
                locs[lang_code] = {}
                added_langs += 1

            unit = locs[lang_code].get("stringUnit", {})
            current_val = unit.get("value", "")

            # Check if it's a placeholder tag like [IT], [DE], etc.
            is_tagged = bool(re.match(r'^\[[A-Z]{2,}\]', current_val))
            is_missing = not current_val

            if is_tagged or is_missing:
                translated = translate(key, lang_code, en_value)
                locs[lang_code]["stringUnit"] = {
                    "state": "translated",
                    "value": translated,
                }
                if is_tagged:
                    fixed_tags += 1
                else:
                    added_langs += 1

            total_entries += 1

    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2, ensure_ascii=False)
        f.write("\n")

    print(f"Processed {total_entries} entries across all languages.")
    print(f"Fixed {fixed_tags} [XX] placeholder tags.")
    print(f"Added {added_langs} missing language entries.")
    print(f"Total keys in catalog: {len(strings)}")

if __name__ == "__main__":
    main()
